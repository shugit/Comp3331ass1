package HelperClass;

public class Path {
	private String start;
	private String end;
	public Path(String a,String b){
		setStart(a);
		setEnd(b);
	}
	public String getStart() {
		return start;
	}
	public void setStart(String start) {
		this.start = start;
	}
	public String getEnd() {
		return end;
	}
	public void setEnd(String end) {
		this.end = end;
	}
	
	
}
